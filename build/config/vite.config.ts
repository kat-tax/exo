import {defineConfig} from 'vite';
import {VitePWA} from 'vite-plugin-pwa';
import {lingui} from '@lingui/vite-plugin';
import react from '@vitejs/plugin-react';
import paths from 'vite-tsconfig-paths';
import sonda from 'sonda/vite';
import cfg from 'config';

export default defineConfig(env => ({
  build: {
    outDir: '../output/client/web',
    sourcemap: true,
    emptyOutDir: true,
    chunkSizeWarningLimit: 1500,
    rollupOptions: {
      output: {
        format: 'es',
        chunkFileNames: '[name]-[hash].js',
      },
      onwarn: (warning, warn) => {
        if (warning.code === 'EVAL') return;
        warn(warning);
      },
    },
  },
  resolve: {
    extensions: [
      '.web.tsx',
      '.web.ts',
      '.web.js',
      '.mjs',
      '.mts',
      '.ts',
      '.tsx',
      '.js',
      '.jsx',
      '.json',
    ],
    alias: {
      'react-native': 'react-native-web',
    },
  },
  plugins: [
    paths(),
    react({
      babel: {
        plugins: [
          '@lingui/babel-plugin-lingui-macro',
          'babel-plugin-react-compiler',
        ],
      },
    }),
    lingui(),
    VitePWA({
      registerType: 'autoUpdate',
      strategies: 'generateSW',
      manifest: {
        name: cfg.APP_NAME,
        short_name: cfg.APP_DISPLAY_NAME,
        description: 'Generated by EXO',
        theme_color: '#000000',
      },
    }),
    {
      name: 'index-html-config',
      transformIndexHtml(html) {
        let out = html;
        for (const [key, value] of Object.entries(cfg)) {
          out = out.replace(new RegExp(`__${key}__`, 'g'), value as string);
        }
        return out;
      },
    },
    sonda({
      open: false,
      sources: true,
      filename: 'index.html',
      outputDir: '../output/inspect/web',
    }),
  ],
  optimizeDeps: {
    exclude: [
      '@evolu/common-web',
    ],
  },
  preview: {
    open: false,
  },
  worker: {
    format: 'es',
  },
  define: {
    __DEV__: JSON.stringify(env.mode === 'development'),
    process: {
      env: {
        NODE_ENV: JSON.stringify(env.mode),
      },
    },
  },
}));
